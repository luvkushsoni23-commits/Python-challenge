# print(a << 1) # Left shift
# print(a >> 1) # Right shift
# '''
# & - it means bitwise AND, it compares each bit of the first operand to the corresponding bit of the second operand. If both bits are 1, the corresponding result bit is set to 1. Otherwise, it is set to 0.
# | - it means bitwise OR, it compares each bit of the first operand to the corresponding
# bit of the second operand. If either bit is 1, the corresponding result bit is set to 1. Otherwise, it is set to 0.
# ^ - it means bitwise XOR, it compares each bit of the first operand to the corresponding
# bit of the second operand. If the bits are different, the corresponding result bit is set to 1. Otherwise, it is set to 0.
# ~ - it means bitwise NOT, it inverts the bits of the operand. Each 0 becomes 1 and each 1 becomes 0.
# << - it means left shift, it shifts the bits of the first operand to the left by the number of positions specified by the second operand. The vacated bits on the right are filled with 0s.
# >> - it means right shift, it shifts the bits of the first operand to the right by
# the number of positions specified by the second operand. The vacated bits on the left are filled with 0s for unsigned numbers and with the sign bit for signed numbers.
# '''
